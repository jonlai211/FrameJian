// Re-render if user switches locale from the overlay while this page is open
chrome.storage.onChanged.addListener((changes, area) => {
  if (area !== "local" || !changes["vn:locale"]) return;
  const next = changes["vn:locale"].newValue === "en" ? "en" : "zh";
  if (next === locale) return;
  locale = next;
  t = STRINGS[locale];
  applyStaticLocale();
  loadAll();
});
