// JSON backup — round-trippable snapshot of every vn:* storage entry.
exportJsonBtn.addEventListener("click", () => {
  chrome.storage.local.get(null, (items) => {
    const backup = {
      app:        "FrameJian",
      version:    "1",
      exportedAt: Date.now(),
      entries:    Object.fromEntries(
        Object.entries(items).filter(([k, v]) => k.startsWith("vn:") && Array.isArray(v?.notes))
      ),
    };
    const blob = new Blob([JSON.stringify(backup, null, 2)], { type: "application/json" });
    const url  = URL.createObjectURL(blob);
    const a    = document.createElement("a");
    a.href     = url;
    a.download = `framejian-backup-${new Date().toISOString().slice(0, 10)}.json`;
    document.body.appendChild(a);
    a.click();
    a.remove();
    URL.revokeObjectURL(url);
  });
});

// Import: merge mode — skip keys that already exist so a re-import doesn't
// clobber edits the user has made since the backup.
importBtn.addEventListener("click", () => importFile.click());

importFile.addEventListener("change", async (e) => {
  const file = e.target.files?.[0];
  if (!file) return;
  importFile.value = ""; // allow re-selecting the same file later
  try {
    const text   = await file.text();
    const backup = JSON.parse(text);
    const entries = backup?.entries && typeof backup.entries === "object" ? backup.entries : null;
    if (!entries || backup.app !== "FrameJian") {
      showToast(t.importPickerInvalid, "error");
      return;
    }
    chrome.storage.local.get(null, (existing) => {
      const toWrite = {};
      let added = 0;
      let skipped = 0;
      Object.entries(entries).forEach(([key, payload]) => {
        if (!key.startsWith("vn:") || !Array.isArray(payload?.notes)) return;
        if (existing[key]) { skipped++; return; }
        toWrite[key] = payload;
        added++;
      });
      if (!added) {
        showToast(t.importDone(0, skipped), "success");
        return;
      }
      chrome.storage.local.set(toWrite, () => {
        if (chrome.runtime.lastError) {
          showToast(t.importFailed + " " + chrome.runtime.lastError.message, "error", 5000);
          return;
        }
        showToast(t.importDone(added, skipped), "success");
        loadAll();
      });
    });
  } catch (err) {
    showToast(t.importFailed + " " + err.message, "error", 5000);
  }
});

